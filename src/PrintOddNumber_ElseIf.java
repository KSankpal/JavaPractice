
public class PrintOddNumber_ElseIf {

	public static void main(String[] args) {

		int a=9,R;
		R = a%2;
		
		
		System.out.println("The given number is "+a);
		if(R!=0) {
			System.out.println("The given number is odd");
		}
		
		
		else {
			System.out.println("The given number is not odd");
		}
	}

}
